-- Classify customers by credit score.
SELECT *,
CASE 
    WHEN CREDIT_SCORE >= 800 THEN "EXCELLENT"
    WHEN CREDIT_SCORE BETWEEN 750 AND 799 THEN "GOOD"
    WHEN CREDIT_SCORE BETWEEN 700 AND 749 THEN "FAIR"
    WHEN CREDIT_SCORE < 700 THEN "POOR"
END AS CREDIT_SCORE_BUCKET
FROM CUSTOMERS;

-- Classify accounts by balance.
SELECT *,
CASE
    WHEN BALANCE >= 300000 THEN "VIP"
    WHEN BALANCE BETWEEN 100000 AND 299999 THEN "PREMIUM"
    WHEN BALANCE < 100000 THEN "REGULAR"
END AS BALANCE_SEGMENT
FROM ACCOUNTS;

-- Classify loans by loan amount.
SELECT *,
CASE
    WHEN LOAN_AMOUNT >= 750000 THEN "HIGH VALUE"
    WHEN LOAN_AMOUNT BETWEEN 300000 AND 749999 THEN "MEDIUM VALUE"
    WHEN LOAN_AMOUNT < 300000 THEN "LOW VALUE"
END AS LOAN_BUCKET
FROM LOANS;

