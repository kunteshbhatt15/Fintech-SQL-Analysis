-- Show customer initials. Example: Rahul Sharma -> RS.
SELECT FIRST_NAME, LAST_NAME, CONCAT(LEFT(FIRST_NAME,1),LEFT(LAST_NAME,1)) AS INITIALS FROM CUSTOMERS;

-- Show email usernames only. Example: rahul@gmail.com -> rahul.
SELECT SUBSTRING_INDEX(EMAIL,'@',1) AS EMAIL_USERNAME FROM CUSTOMERS;

-- Show customer names and email values. If email is null, show NO EMAIL.
SELECT CONCAT(FIRST_NAME," ",LAST_NAME) AS FULL_NAME, EMAIL, COALESCE(NULLIF(EMAIL,''),"NO EMAIL") AS EMAIL_STATUS FROM CUSTOMERS;

-- Show customer names and referral codes. If referral code is null, show NOT_REFERRED.
SELECT CONCAT(FIRST_NAME," ",LAST_NAME) AS FULL_NAME, COALESCE(NULLIF(REFERRAL_CODE,''),"NOT_REFERRED") AS REFERRAL_STATUS FROM CUSTOMERS;

-- Find customers whose email is null.
