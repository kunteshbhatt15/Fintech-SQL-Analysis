WHERE CUSTOMER_ID NOT IN (SELECT CUSTOMER_ID FROM LOANS);

-- Find customers whose credit score is above the average credit score of customers in the same city.
SELECT CONCAT(FIRST_NAME," ",LAST_NAME) AS CUSTOMER_NAME, CITY, CREDIT_SCORE FROM CUSTOMERS AS C
WHERE CREDIT_SCORE >= (SELECT AVG(CREDIT_SCORE) FROM CUSTOMERS WHERE CITY = C.CITY);

-- Find accounts having the lowest balance within their account type.
