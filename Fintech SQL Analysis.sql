-- Find the top two customers by balance within each account type.
WITH TOP_CUSTOMERS AS (
    SELECT
        CONCAT(FIRST_NAME," ",LAST_NAME) AS FULL_NAME,
        BALANCE,
        ACCOUNT_TYPE,
        DENSE_RANK() OVER(PARTITION BY ACCOUNT_TYPE ORDER BY BALANCE DESC) AS BALANCE_RANK
    FROM ACCOUNTS AS A
    INNER JOIN CUSTOMERS AS C ON C.CUSTOMER_ID = A.CUSTOMER_ID
)
SELECT FULL_NAME, BALANCE, ACCOUNT_TYPE, BALANCE_RANK FROM TOP_CUSTOMERS
WHERE BALANCE_RANK <= 2;

