-- Find account types whose average balance is greater than the overall average account balance.
SELECT ACCOUNT_TYPE, AVG(BALANCE) AS AVERAGE_BALANCE FROM ACCOUNTS
GROUP BY ACCOUNT_TYPE
HAVING AVG(BALANCE) > (SELECT AVG(BALANCE) FROM ACCOUNTS);

-- Find customers whose account balance is greater than the average balance of customers in the same city.
SELECT CONCAT(FIRST_NAME," ",LAST_NAME) AS CUSTOMER_NAME, CITY, BALANCE FROM CUSTOMERS AS C
INNER JOIN ACCOUNTS AS A ON C.CUSTOMER_ID = A.CUSTOMER_ID
WHERE BALANCE > (
    SELECT AVG(A2.BALANCE)
    FROM ACCOUNTS A2
    INNER JOIN CUSTOMERS C2 ON C2.CUSTOMER_ID = A2.CUSTOMER_ID
    WHERE C2.CITY = C.CITY
